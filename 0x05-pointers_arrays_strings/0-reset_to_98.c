#include "main.h"

/**
 * reset_to_98 - Resets the value of an integer to 98
 * @n: Pointer to the integer to be reset
 *
 * This function takes a pointer to an integer and modifies the value it
 * points to by assigning it the value 98. The original value is effectively
 * reset to 98.
 */
void reset_to_98(int *n)
{
	*n = 98;
}
